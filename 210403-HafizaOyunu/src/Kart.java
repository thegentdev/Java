
public class Kart {
	private char deger;
	private boolean durum = false;

	public Kart(char deger) {

		this.deger = deger;

	}

	public char getDeger() {
		return deger;
	}

	public void setDeger(char deger) {
		this.deger = deger;
	}

	public boolean isDurum() {
		return durum;
	}

	public void setDurum(boolean durum) {
		this.durum = durum;
	}
	
}
